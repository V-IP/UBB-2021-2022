def afiseaza_meniu_principal():
    '''
    Functie care afiseaza meniul de optiuni
    '''
    print("0 - Exit")
    print("1 - Adaugare de noi tranzactii")
    print("2 - Stergere")
    print("3 - Cautari")
    print("4 - Rapoarte")
    print("5 - Filtrare")
    print("6 - Undo")

def afiseaza_meniu_1():
    '''
    Functie care afiseaza iteratiile de la functionalitatea 1: Adaugare de noi tranzactii
    '''
    print("1 - Adauga tranzactie")
    print("2 - Actualizare tranzactie")

def afiseaza_meniu_2():
    '''
    Functie care afiseaza iteratiile de la functionalitatea 2: Stergere
    '''
    print("1 - Șterge toate tranzacțiile de la ziua specificată")
    print("2 - Șterge tranzacțiile dintr-o perioadă dată (se dă ziua de început și sfârșit)")
    print("3 - Șterge toate tranzacțiile de un anumit tip")

def afiseaza_meniu_3():
    '''
    Functie care afiseaza iteratiile de la functionalitatea 3: Cautari
    '''
    print("1 - Tipareste toate tranzactiile")
    print("2 - Tipărește tranzacțiile cu sume mai mari decât o sumă dată")
    print("3 - Tipărește toate tranzacțiile efectuate înainte de o zi și mai mari decât o sumă (se dă suma și ziua)")
    print("4 - Tipărește tranzacțiile de un anumit tip")

def afiseaza_meniu_4():
    '''
    Functie care afiseaza iteratiile de la functionalitatea 4: Rapoarte
    '''
    print("1 - Suma totală a tranzacțiilor de un anumit tip")
    print("2 - Soldul contului la o dată specificată")
    print("3 - Tipărește toate tranzacțiile de un anumit tip ordonat după sumă")

def afiseaza_meniu_5():
    '''
    Functie care afiseaza iteratiile de la functionalitatea 5: Filtrare
    '''
    print("1 - Elimină toate tranzacțiile de un anumit tip")
    print("2 - Elimină toate tranzacțiile mai mici decât o sumă dată care au tipul specificat")